function containsProfanity(text) {
    if (!text) return false;
    const lower = text.toLowerCase();
    const clean = lower.replace(/[^a-zA-Zа-яА-ЯёЁ]/g, '');
    for (let word of profanityList) {
        if (lower.includes(word) || clean.includes(word)) {
            return true;
        }
    }
    return false;
}